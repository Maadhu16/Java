// Variables are containers for storing data values.

//In Java, create a variable by specifying its Datatype, giving it a name, and optionally assigning a value.

class variables{
    public static void main(String args[])
    {
        int a = 10;                                // int => Datatype, a => Variable name, 10 => value
        String name = "Maadhu";                    //String => Datatype, name => Variable name, Maadhu => value
        System.out.println(a);
        System.out.println(name);
    }

}
